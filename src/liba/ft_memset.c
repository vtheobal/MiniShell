#include "../inc/minishell.h"

void	*ft_memset(void *b, int c, size_t n)
{
	int		i;
	char	*ptr;

	ptr = (char *) b;
	i = 0;
	while (n > 0)
	{
		ptr[i] = c;
		n--;
		i++;
	}
	return (ptr);
}
