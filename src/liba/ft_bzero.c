#include "../inc/minishell.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*ptr;

	ptr = (char *) s;
	i = 0;
	while (n > 0)
	{
		ptr[i] = 0;
		n--;
		i++;
	}
}
